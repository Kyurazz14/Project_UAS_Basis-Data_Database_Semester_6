  document.addEventListener("DOMContentLoaded", function() {
    const jenisSepatuSelect = document.querySelector('select[name="jenis_sepatu"]');
    const namaSepatuSelect = document.querySelector('select[name="nama_sepatu"]');
    const jumlahPembelianInput = document.querySelector('input[name="jumlah_pembelian"]');
    const totalHargaInput = document.querySelector('input[name="total_harga"]');

    // Update total harga when jenis sepatu or jumlah pembelian changes
    function updateTotalHarga() {
      const hargaPerSepatu = calculateHargaPerSepatu(jenisSepatuSelect.value, namaSepatuSelect.value);
      const jumlahPembelian = Number(jumlahPembelianInput.value);
      const totalHarga = hargaPerSepatu * jumlahPembelian;
      totalHargaInput.value = totalHarga;
    }

    // Calculate harga per sepatu based on jenis sepatu and nama sepatu
    function calculateHargaPerSepatu(jenisSepatu, namaSepatu) {
      if (jenisSepatu === "Sepatu Lokal") {
        if (namaSepatu === "Sepatu SL 1") {
          return 95000;
        } else if (namaSepatu === "Sepatu SL 2") {
          return 150000;
        } else if (namaSepatu === "Sepatu SL 3") {
          return 350000;
        }
      } else if (jenisSepatu === "Sepatu Import") {
        if (namaSepatu === "Sepatu SI 1") {
          return 300000;
        } else if (namaSepatu === "Sepatu SI 2") {
          return 500000;
        } else if (namaSepatu === "Sepatu SI 3") {
          return 1000000;
        }
      } else if (jenisSepatu === "Sepatu Second") {
        if (namaSepatu === "Sepatu SC 1") {
          return 30000;
        } else if (namaSepatu === "Sepatu SC 2") {
          return 50000;
        } else if (namaSepatu === "Sepatu SC 3") {
          return 80000;
        }
      }
    }

    jenisSepatuSelect.addEventListener("change", function() {
      // Reset nama sepatu select options
      namaSepatuSelect.innerHTML = '<option value="">Pilih Nama Sepatu</option>';
      const jenisSepatu = jenisSepatuSelect.value;

      // Populate nama sepatu select options based on jenis sepatu
      if (jenisSepatu === "Sepatu Lokal") {
        namaSepatuSelect.innerHTML += '<option value="Sepatu SL 1">Sepatu SL 1 - Rp. 95.000</option>';
        namaSepatuSelect.innerHTML += '<option value="Sepatu SL 2">Sepatu SL 2 - Rp. 150.000</option>';
        namaSepatuSelect.innerHTML += '<option value="Sepatu SL 3">Sepatu SL 3 - Rp. 350.000</option>';
      } else if (jenisSepatu === "Sepatu Import") {
        namaSepatuSelect.innerHTML += '<option value="Sepatu SI 1">Sepatu SI 1 - Rp. 300.000</option>';
        namaSepatuSelect.innerHTML += '<option value="Sepatu SI 2">Sepatu SI 2 - Rp. 500.000</option>';
        namaSepatuSelect.innerHTML += '<option value="Sepatu SI 3">Sepatu SI 3 - Rp. 1.000.000</option>';
      } else if (jenisSepatu === "Sepatu Second") {
        namaSepatuSelect.innerHTML += '<option value="Sepatu SC 1">Sepatu SC 1 - Rp. 30.000</option>';
        namaSepatuSelect.innerHTML += '<option value="Sepatu SC 2">Sepatu SC 2 - Rp. 50.000</option>';
        namaSepatuSelect.innerHTML += '<option value="Sepatu SC 3">Sepatu SC 3 - Rp. 80.000</option>';
      }

      // Update total harga
      updateTotalHarga();
    });

    namaSepatuSelect.addEventListener("change", function() {
      // Update total harga
      updateTotalHarga();
    });

    jumlahPembelianInput.addEventListener("input", function() {
      // Update total harga
      updateTotalHarga();
